char	*ft_strrchr(const char *s, int c)
{
	char	*cr;
	char	cc;

	cr = 0;
	cc = (char) c;
	while (*s != '\0')
	{
		if (*s == cc)
			cr = (char *)s;
		s++;
	}
	if (cc == '\0')
		return ((char *)s);
	return (cr);
}
